
//Equality Operators

//Strict equality operator(same type same value)
let x = 1;
console.log(x === 1);
console.log(x !== 1);
console.log('1' === 1);

//Loose equality
console.log(x == 1);
console.log('1' == 1); //autoamtically converts to String
console.log(true == 1); //autoamtically converts to boolean
